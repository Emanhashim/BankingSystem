private void insertIntoCustomer() {
        DefaultTableModel facultyTblModel = (DefaultTableModel) tblcustomerlist.getModel();
        if (btnNew.getText().equalsIgnoreCase("New")) {
            int emptrow = facultyTblModel.getRowCount() + 1;
            facultyTblModel.setRowCount(emptrow);
            btnNew.setText("Save");
        } else {
            int newrow = facultyTblModel.getRowCount()-1;
            try {
                rst.moveToInsertRow();
                rst.updateString("Cust_ID", facultyTblModel.getValueAt(newrow, 1).toString());
                rst.updateString("First_Name", facultyTblModel.getValueAt(newrow, 2).toString());
                rst.updateString("Last_Name", facultyTblModel.getValueAt(newrow, 3).toString());
                rst.updateString("Sex", facultyTblModel.getValueAt(newrow, 4).toString());
                rst.updateString("Address", facultyTblModel.getValueAt(newrow, 5).toString());
                rst.updateString("Phone_Number", facultyTblModel.getValueAt(newrow, 5).toString());
                rst.insertRow();
                rst.moveToCurrentRow();
                JOptionPane.showMessageDialog(this, "Record saved successfully.");
                btnNew.setText("New");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Database server error" + e.getMessage());
            }
        }
    }
private void updatecustomer() {
        DefaultTableModel facultyTblModel = (DefaultTableModel) tblcustomerlist.getModel();
        try {
            int resp = JOptionPane.showConfirmDialog(this, "Are you sure you want to update the selected record?");
            if (resp == JOptionPane.YES_OPTION) {
                int row = tblcustomerlist.getSelectedRow() + 1;
                rst.absolute(row);
                rst.updateString("Cust_ID", facultyTblModel.getValueAt(tblcustomerlist.getSelectedRow(), 1).toString());
                rst.updateString("First_Name", facultyTblModel.getValueAt(tblcustomerlist.getSelectedRow(), 2).toString());
                rst.updateString("Last_Name", facultyTblModel.getValueAt(tblcustomerlist.getSelectedRow(), 3).toString());
                rst.updateString("Sex", facultyTblModel.getValueAt(tblcustomerlist.getSelectedRow(), 4).toString());
                rst.updateString("Addressr", facultyTblModel.getValueAt(tblcustomerlist.getSelectedRow(), 5).toString());
                 rst.updateString("Phone_Number", facultyTblModel.getValueAt(tblcustomerlist.getSelectedRow(), 5).toString());
                rst.updateRow();
                JOptionPane.showMessageDialog(this, "Record updated successfully.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Database server error" + e.getMessage());
        }
    }private void deleteFromCustomer() {
        try {
            int resp = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete the selected record?");
            if (resp == JOptionPane.YES_OPTION) {
                int row = tblcustomerlist.getSelectedRow() + 1;
                rst.absolute(row);
                rst.deleteRow();
                JOptionPane.showMessageDialog(this, "Record deleted successfully.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Database server error" + e.getMessage());
        }
    }













