    private void readCustomer() {
       
        DefaultTableModel facultyTblModel = (DefaultTableModel) tblcustomerlist.getModel();
        facultyTblModel.setRowCount(0);
        String sql = "Select * From Customer order by First_Name ASC";
        try {
            conn = connection.getConn();
            stmt = conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            rst = stmt.executeQuery(sql);
            int row = 0;
            while (rst.next()) {
                facultyTblModel.addRow(new Object[]{rst.getString("Cust_ID"),rst.getString("First_Name"), rst.getString("Last_Name"), rst.getString("Sex"), rst.getString("Address"), rst.getString("Phone_Number")});
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database server error" + e.getMessage());
        } finally {
        }

    }
