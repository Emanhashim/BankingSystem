    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        if (!txtAccNoSender.getText().equalsIgnoreCase("")) {
            String sql = "Select * from customerAccount where Account_no = ?";
            try {
                conn = connection.getConn();
                pst = conn.prepareStatement(sql);
                pst.setInt(1, Integer.parseInt(txtAccNoSender.getText()));
                rst = pst.executeQuery();
                while (rst.next()) {
                    txtFullNameSender.setText(rst.getString("FullName"));
                    //
                    SenderCurrBalance = rst.getDouble("Current_balance");
                    SenderAccNo = rst.getInt("Account_no");
                    //
                    txtbalance.setText(Double.toString(SenderCurrBalance));

                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnsearchActionPerformed

