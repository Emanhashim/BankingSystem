    private void btnTransferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTransferActionPerformed
         if(Double.parseDouble(txtAmount.getText())< SenderCurrBalance){
             String sqlSend = "Update Acount set Current_Balance = ? where Account_No = ?";
             String sqlRec = "Update Acount set Current_Balance = ? where Account_No = ?";
             String sqlTran = "Insert into Transfer (Employee_ID, reciverAccount_Id, SenderAccount_Id,transfer_date, Amount) values(?,?,?,NOW(),?)";
             try {
                 conn = connection.getConn();
                 conn.setAutoCommit(false);
                 Double amount = Double.parseDouble(txtAmount.getText());
                 //sender
                 int ar = 0;
                 pst = conn.prepareStatement(sqlSend);
                 pst.setDouble(1, SenderCurrBalance - Double.parseDouble(txtAmount.getText()));
                 pst.setInt(2, SenderAccNo);
                 pst.executeUpdate();
                 ar ++;
                 //reciever
                 pst=conn.prepareStatement(sqlRec);
                 pst.setDouble(1, RecieverCurrBalance+amount);
                 pst.setInt(2, RecieverAccNo);
                 pst.executeUpdate();
                 ar ++;
                 //Transfer
                 pst=  conn.prepareStatement(sqlTran);
                 pst.setString(1, UserInfo.EmpId);
                 pst.setInt(2, RecieverAccNo);
                 pst.setInt(3, SenderAccNo);
                 pst.setDouble(4, amount);
                 pst.executeUpdate();
                 ar ++;
                 conn.commit();
                 if(ar>0){
                     JOptionPane.showMessageDialog(this, "Transfer success.");
                 }else {
                     JOptionPane.showMessageDialog(this, "Sorry, Transfer faild.");
                 }
                 
             } catch (Exception e) {
                 JOptionPane.showMessageDialog(this, e.getMessage());
             }
                         
             
        }else {
             JOptionPane.showMessageDialog(this, "You dont have sufficient balance to transfer.");
         }
    }//GEN-LAST:event_btnTransferActionPerformed

